def main():
    conn = sqlite3.connect("questions.db")
    cur = conn.cursor()

    inserted = 0
    skipped = 0

    for q, oa, ob, oc, od, ans in QUESTIONS:
        # 檢查是否已存在（以題目去重）
        cur.execute("SELECT id FROM questions WHERE question = ?", (q,))
        if cur.fetchone():
            skipped += 1
            continue

        cur.execute(
            "INSERT INTO questions (category, question, option_a, option_b, option_c, option_d, answer, source_url, article_date) "
            "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)",
            (CATEGORY, q, oa, ob, oc, od, ans, SOURCE_URL, DATE),
        )
        inserted += 1

    conn.commit()
    conn.close()
    print(f"完成！新增 {inserted} 題，跳過（已存在）{skipped} 題。")


if __name__ == "__main__":
    main()
